package ConditionalStatement;

public class IfStatement {

	public static void main(String[] args) {
		
		int age=15;
		// If statement 
		if(age>=18) {
			 System.out.println("Eligiable for vote-- only if ");	
		}
		
		System.out.println("Program excuted---only if ");
		

		// if else statement 
		if(age>=18) {
			System.out.println("Eligible for vote --if & else");
		}
		else {
			
		System.out.println("Not Eligible for vote");
		}
		
		System.out.println("Program executed --if & else ");
			
		
		
	}

}
